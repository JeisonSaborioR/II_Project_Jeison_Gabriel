    private void BtoLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtoLogInActionPerformed
        // TODO add your handling code here:
        
        
        if(TxtEmailAddress.getText().isEmpty()){
            JOptionPane.showMessageDialog(this , "Empty email");
            return;
        }
        if(TxtPassWord.getText().isEmpty()){
            JOptionPane.showMessageDialog(this , "Empty password");
            return;
        }
        
        String email = TxtEmailAddress.getText();
        String passWord = new String (TxtPassWord.getPassword());
        passWord = DigestUtils.md5Hex(passWord);
        System.out.println(passWord);
        for(int i = 0; i < GlobalVariables.getInstance().peopleList.size();i++){
            if(GlobalVariables.getInstance().peopleList.get(i).getEmailAddress().equals(email)){
                if(GlobalVariables.getInstance().peopleList.get(i).getPassWord().equals(passWord)){
                    if(GlobalVariables.getInstance().peopleList.get(i).getType().equals("Customer")){
                        if(GlobalVariables.getInstance().hotel == null){
                            CustomerMain customerMain = new CustomerMain();
                            customerMain.setVisible(true);
                            GlobalVariables.getInstance().person = GlobalVariables.getInstance().peopleList.get(i);
                            this.setVisible(false);
                            return;
                        }else{
                            ReservationMenu reservationMenu = new ReservationMenu();
                            reservationMenu.setVisible(true);
                            GlobalVariables.getInstance().person = GlobalVariables.getInstance().peopleList.get(i);
                            this.dispose();
                            return;
                        }
                       
                    }else if(GlobalVariables.getInstance().peopleList.get(i).getType().equals("Staff")){
                        StaffMain staffMain = new StaffMain();
                        staffMain.setVisible(true);
                        GlobalVariables.getInstance().person = GlobalVariables.getInstance().peopleList.get(i);
                        this.setVisible(false);
                        return;
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this,"Email or password incorrect");
                }
            }
        }
        
    }//GEN-LAST:event_BtoLogInActionPerformed

