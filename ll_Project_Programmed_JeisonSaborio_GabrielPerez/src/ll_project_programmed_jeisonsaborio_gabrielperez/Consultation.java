public class Consultation {
    private int cant;
    private int month;

    public Consultation(int cant, int month) {
        this.cant = cant;
        this.month = month;
    }

    public int getCant() {
        return cant;
    }

    public void setCant(int cant) {
        this.cant = cant;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }
    
    
}
